export async function fetchEnergyRetail(): Promise<void> {
  console.log("[Energy Retail] Building curated retail gasoline price data...");

  const countries: Record<string, DataPoint[]> = {};

  for (const [countryCode, prices] of Object.entries(RETAIL_GASOLINE_PRICES)) {
    const baseValue = prices[BASE_YEAR];
    if (!baseValue) {
      console.warn(`  Skipping ${countryCode}: no base year (${BASE_YEAR}) value`);
      continue;
    }

    const points: DataPoint[] = [];
    for (let year = 2015; year <= 2025; year++) {
      const value = prices[year] ?? null;
      points.push({
        year,
        value,
        indexed:
          value != null
            ? Math.round((value / baseValue) * 100 * 100) / 100
            : null,
      });
    }

    countries[countryCode] = points;
  }

  const output: CategoryDataFile = {
    baseYear: BASE_YEAR,
    lastUpdated: new Date().toISOString().split("T")[0],
    source:
      "Curated retail gasoline prices (USD/liter) - GlobalPetrolPrices.com, IEA, World Bank",
    countries,
  };

  mkdirSync(OUT_DIR, { recursive: true });
  writeFileSync(OUT_FILE, JSON.stringify(output, null, 2));
  console.log(
    `  Wrote ${Object.keys(countries).length} countries to ${OUT_FILE}`,
  );
}

if (import.meta.url === `file:///${process.argv[1].replace(/\\/g, "/")}`) {
  fetchEnergyRetail();
}
