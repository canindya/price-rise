/**
 * Generates MOSPI CPI data for India.
 *
 * MOSPI (Ministry of Statistics and Programme Implementation) publishes
 * monthly CPI indices on cpi.mospi.gov.in. Since there is no clean REST API,
 * this script uses curated historical data based on official MOSPI publications.
 *
 * The data uses base year 2012=100 (the official series) and is stored as-is.
 * The frontend hook normalizes to any desired base period.
 *
 * To update: download latest Excel from cpi.mospi.gov.in and update the
 * MOSPI_DATA arrays below. Data is available with ~6-week lag.
 *
 * Outputs to public/data/mospi_cpi.json
 */

