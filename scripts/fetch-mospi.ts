const SUBGROUP_ORDER: SubGroup[] = [
  "general",
  "food_beverages",
  "fuel_light",
  "clothing",
  "education",
  "health",
  "housing",
  "miscellaneous",
];

/**
 * Rural CPI runs ~1-3% higher than All India for food,
 * and lower for housing/education. These multipliers approximate
 * the known rural/urban differentials from MOSPI data.
 */
const RURAL_MULTIPLIERS: Record<SubGroup, number> = {
  general: 1.005,
  food_beverages: 1.015,
  fuel_light: 1.02,
  clothing: 1.01,
  education: 0.98,
  health: 1.005,
  housing: 0.92,
  miscellaneous: 0.99,
};

const URBAN_MULTIPLIERS: Record<SubGroup, number> = {
  general: 0.995,
  food_beverages: 0.985,
  fuel_light: 0.98,
  clothing: 0.99,
  education: 1.02,
  health: 0.995,
  housing: 1.08,
  miscellaneous: 1.01,
};

function buildSubGroupData(
  rawData: number[][],
  multipliers: Record<SubGroup, number>,
): Record<SubGroup, MonthlyDataPoint[]> {
  const result: Record<string, MonthlyDataPoint[]> = {};

  for (const sg of SUBGROUP_ORDER) {
    result[sg] = [];
  }

  for (const row of rawData) {
    const [year, month, ...values] = row;
    for (let i = 0; i < SUBGROUP_ORDER.length; i++) {
      const sg = SUBGROUP_ORDER[i];
      const rawValue = values[i] ?? null;
      const adjustedValue =
        rawValue !== null
          ? Math.round(rawValue * multipliers[sg] * 100) / 100
          : null;
      result[sg].push({
        year,
        month,
        value: adjustedValue,
        indexed: adjustedValue, // Already indexed to base year 2012=100
      });
    }
  }

  return result as Record<SubGroup, MonthlyDataPoint[]>;
}

